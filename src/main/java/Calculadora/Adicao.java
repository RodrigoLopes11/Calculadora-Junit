public class Adicao {
/**
	 * Método criado para a operação de Adição.
	 * 
	 * @param a Int para o valor do primeiro número.
	 * @param b Int para o valor do segundo número.
	 * @return Int para o valor do resultado da Adição.
	 */
    public int soma(int a, int b){  
       return a + b; 
    }
}