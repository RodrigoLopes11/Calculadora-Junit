package Calculadora;
/**
 * @author lopes
 * @version 1.2
 * @since Release 1.2 da aplicação - 17/11/2023
 * 
 *  Esta classe é correspondente a operação de Multiplicação.
 */
public class Multiplicacao {
/**
	 * Método criado para a operação de Multiplicação.
	 * 
	 * @param a Int para o valor do primeiro número.
	 * @param b Int para o valor do segundo número.
	 * @return Int para o valor do resultado da Multiplicação.
	 */
    public int multi(int a, int b){
        return a * b;
    }
}